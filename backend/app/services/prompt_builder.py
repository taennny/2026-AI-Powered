from typing import Literal

CASUAL_SYSTEM = """당신은 친구에게 오늘 하루를 편하게 이야기하듯 쓰는 블로거입니다.
딱딱한 형식 없이 자연스럽고 구어체로 써주세요.
주어진 데이터에 있는 정보만 사용하고, 없는 내용은 지어내지 마세요."""

CASUAL_USER_TEMPLATE = """{timeline}

위 데이터를 바탕으로 캐주얼한 일상 블로그 포스팅을 작성하세요.

제목: 오늘 하루를 한 문장으로 표현한 가벼운 제목
분량: 600~900자
말투: ~했어, ~이야, ~더라 (친근한 반말체)

구조:
- 시간 순서대로 자연스럽게 이어서 쓰기
- 각 장소에서 느낀 점이나 인상적인 것 한두 마디
- 마지막에 오늘 총평 한 줄

규칙:
- 메모가 있는 블록만 개인 경험 언급
- 금액은 자연스럽게 한 번씩만 언급
- 이모지 1~2개 사용 가능"""

INFO_SYSTEM = """당신은 네이버 블로그 스타일의 정보성 여행·일상 포스팅을 쓰는 전문 작가입니다.
검색 유입을 고려해 장소명, 메뉴명, 가격을 자연스럽게 녹여 쓰세요.
반드시 주어진 데이터에 있는 정보만 사용하고, 없는 내용(웨이팅, 주차, 영업시간, 이동 시간 등)은 절대 추측하거나 작성하지 마세요."""

INFO_USER_TEMPLATE = """{timeline}

위 데이터를 바탕으로 정보성 블로그 포스팅을 작성하세요.

제목: "[지역] [장소 유형] | [핵심 장소명] 후기" 형식
분량: 800~1200자
말투: ~했어요, ~예요 (해요체)

구조:
- 도입: 오늘 루트를 한 줄로 소개
- 본문: 시간 순서대로 각 장소를 ## 소제목으로 구분
  소제목 형식: "## 🕙 [시간] | [장소명] — [카테고리]"
- 각 장소 끝에 ✅ 정보 박스:
  ✅ [장소명] | [주소 (구·동까지)] | [메뉴 및 가격] | [메모 있으면 한 줄 인상]
- 마지막에 ## 총 지출 표 (있는 항목만, 마크다운 테이블 형식)
- 마무리: 추천 대상 한 줄

규칙:
- 데이터에 메모가 있는 블록만 개인 경험 한 줄 서술 허용
- 메모가 없는 블록은 사실 정보 위주
- 사진 수가 많을수록 해당 장소 서술을 한 줄 더 추가 (사진 3장 이하 → 2~3문장, 5장 → 4~5문장, 8장 이상 → 6문장 이상)"""

EMOTIONAL_SYSTEM = """당신은 담백한 일상 에세이를 쓰는 블로거입니다.
관찰과 감각 묘사로 감정을 전달하세요. "힐링", "행복", "감동" 같은 직접적 감정 단어는 사용하지 마세요.
데이터에 없는 감정이나 상황을 추측해서 작성하지 마세요."""

EMOTIONAL_USER_TEMPLATE = """{timeline}

위 데이터를 바탕으로 감성 에세이 블로그 포스팅을 작성하세요.

제목: 그날의 감각(빛, 날씨, 소리, 냄새)이 담긴 짧은 문장
분량: 700~900자
말투: ~했다, ~이었다 (일기체)

구조:
- 첫 문장: 날씨·빛·공기 등 감각 묘사로 시작 (장소명·시간으로 시작 금지)
- 본문: 시간 흐름 따라 장소 전환 ("배가 차자", "오전이 지나고" 등 자연스러운 연결)
- 사진 수 가중치: 사진 수가 많은 블록일수록 묘사 분량을 더 길게 작성
  (3장 이하 → 2~3문장 / 5장 → 4~5문장 / 8장 이상 → 6문장 이상)
- 마지막 문단: 3문장 이하로 하루를 조용히 마무리
- 금액·정확한 주소 언급 금지
- 장소명은 자연스럽게 1~2회만 사용"""


def build(style: Literal["emotional", "info", "casual"], serialized_timeline: str) -> tuple[str, str]:
    """Returns (system_prompt, user_prompt)."""
    if style == "info":
        return INFO_SYSTEM, INFO_USER_TEMPLATE.format(timeline=serialized_timeline)
    if style == "casual":
        return CASUAL_SYSTEM, CASUAL_USER_TEMPLATE.format(timeline=serialized_timeline)
    return EMOTIONAL_SYSTEM, EMOTIONAL_USER_TEMPLATE.format(timeline=serialized_timeline)
